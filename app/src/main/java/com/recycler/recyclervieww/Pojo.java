package com.recycler.recyclervieww;

public class Pojo {


    int recyclerImg;
    String recyclerText;

    public Pojo(int recyclerImg, String recyclerText) {
        this.recyclerImg = recyclerImg;
        this.recyclerText = recyclerText;
    }

    public int getRecyclerImg() {
        return recyclerImg;
    }

    public void setRecyclerImg(int recyclerImg) {
        this.recyclerImg = recyclerImg;
    }

    public String getRecyclerText() {
        return recyclerText;
    }

    public void setRecyclerText(String recyclerText) {
        this.recyclerText = recyclerText;
    }
}
